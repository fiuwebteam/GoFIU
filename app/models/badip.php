<?php 
class BadIp extends AppModel {
//---------------------------------------------------------------------------------
	var $name = 'BadIp';
	
	//---------------------------------------------------------------------------------
    var $validate = array( 'ip' => "isUnique" );
	//---------------------------------------------------------------------------------
 	function flagIps($ips) {
 		foreach($ips as $key => $value) {
 			
 			$badIp = array(
				"BadIp" => array(
					"ip" => $value
				)
			);
 			$this->create();
			$this->save($badIp);
 		} 	
 	}
 	
 	
	//---------------------------------------------------------------------------------
}

